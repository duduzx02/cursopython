'''

Condições IF, ELIF e ELSE

'''

if False:
    print('Verdadeiro.')
elif False:
    print('Agora é verdadeiro.')
elif False:
    print('Mais um verdadeiro.')
else:
    print('Não é verdadeiro')

'''

* O bloco elif é usado quando se faz necessário checar outras condições ou variações da mesma condição. O bloco elif depende do bloco if, ou seja, é necessário primeiro usar o bloco if e somente se a expressão do bloco if for falsa, o bloco elif será verificado.

* O bloco else só é executado quando nenhuma condição de if ou elif for verdadeira. Por isso, quando precisamos de um valor padrão ou de uma resposta contrária à condição, é necessário adicionar o bloco else.
'''